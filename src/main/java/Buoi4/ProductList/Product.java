package Buoi4.ProductList;

public class Product {
    public String id;
    public String name;
    public double price;
    public int quantity;
    public String brand;

    public Product(String id, String name, double price,int quantity, String brand) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.quantity = quantity;
        this.brand = brand;

    }

}
